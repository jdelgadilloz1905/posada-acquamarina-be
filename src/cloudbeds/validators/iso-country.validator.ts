@ValidatorConstraint({ async: false })
export class IsISOCountryConstraint implements ValidatorConstraintInterface {
  validate(countryCode: string) {
    if (!countryCode) return false;
    return ISO_COUNTRY_CODES.includes(countryCode.toUpperCase());
  }

  defaultMessage() {
    return 'Country code must be a valid ISO 3166-1 alpha-2 code (e.g., US, VE, CA)';
  }
}

export function IsISOCountry(validationOptions?: ValidationOptions) {
  return function (object: Object, propertyName: string) {
    registerDecorator({
      target: object.constructor,
      propertyName: propertyName,
      options: validationOptions,
      constraints: [],
      validator: IsISOCountryConstraint,
    });
  };
}
